#creating a class
class Myclass:
    def add(a,b):
      return a+b
    def sub(a,b):
      return a-b
    def mul(a,b):
      return a*b
    def div(a,b):
      return a/b
    def mod(a,b):
      return a%b
obj1=Myclass
print(obj1.add(17,5))
print(obj1.sub(17,5))
print(obj1.mul(17,5))
print(obj1.div(17,5))
print(obj1.mod(17,5))

